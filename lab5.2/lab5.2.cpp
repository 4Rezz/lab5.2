long double fact(int N)
{
	if (N < 0)
		return 0;
	if (N == 0)
		return 1;
	else
		return N * fact(N - 1);
}
double S(const double x, const double eps, int& n, double& s)
{
	n = 0;
	double a = -(1 / x);
	s = a;
	do {
		n++;
		a = A(x, n, a);
		s += a;
	} while (abs(a) >= eps);
	return s;
}
double A(const double x, const int n, double& a)
{
	double R = (pow(-1, n + 1)) / ((2 * n + 1) * pow(x, 2 * n + 1));
	a *= R;
	return a;
}


